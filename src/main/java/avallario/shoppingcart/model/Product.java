package avallario.shoppingcart.model;

public class Product {

    public String id;
    public String name;
    public Double price;

    public Product() {
    }

    public Product(String id, String name, Double price) {
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public Double getPrice() {
        return price;
    }
}
